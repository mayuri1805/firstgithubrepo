# range(end position)
for i in range(5):
    print(i,end=' ')
print()

# range(start,end)
for i in range(1,5):
    print(i,end=' ')
print()

# range(start,end,step) ex-even no, odd no
for i in range(1,7,2):
    print(i,end=' ')
print()

# for reverse the number
for i in range(5,1,-1):
    print(i, end=' ')
print()

# for 5 table
for i in range(5,51,5):
    print(i, end=' ')
print()

# for reverse the 5 table
for i in range(50,4,-5):
    print(i, end=' ')
print()






